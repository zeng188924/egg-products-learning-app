// 生成PWA图标的脚本
const fs = require('fs');
const path = require('path');

// 简单的PNG图标生成器（使用base64编码的简单图标）
const createIcon = (size) => {
  // 创建一个简单的纯色PNG图标
  // 这是一个最小的有效PNG文件（8x8像素，绿色）
  // 对于实际使用，应该使用真正的图标
  const pngBase64 = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, size >> 8, 0x00, 0x00, 0x00, size & 0xFF,
    0x08, 0x06, 0x00, 0x00, 0x00, 0xE0, 0x77, 0x3D,
    0xF8, 0x00, 0x00, 0x00, 0x1A, 0x49, 0x44, 0x41,
    0x54, 0x38, 0x8D, 0x63, 0x60, 0x18, 0x05, 0xA3,
    0x60, 0x14, 0x8C, 0x02, 0x08, 0x00, 0x00, 0xFF,
    0xFF, 0x03, 0x00, 0x01, 0x00, 0x01, 0x8D, 0x8A,
    0x4E, 0x32, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45,
    0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
  ]);
  return pngBase64;
};

// 创建图标目录
const iconDir = path.join(__dirname, 'public/icons');
if (!fs.existsSync(iconDir)) {
  fs.mkdirSync(iconDir, { recursive: true });
}

// 生成各种尺寸的图标
const sizes = [72, 96, 128, 144, 152, 192, 384, 512];
sizes.forEach(size => {
  const icon = createIcon(size);
  fs.writeFileSync(path.join(iconDir, `icon-${size}x${size}.png`), icon);
  console.log(`Created icon-${size}x${size}.png`);
});

console.log('所有图标已创建完成！');
